function togglebpReservoirEngineerParagraph() {
    // get the section that is to be hidden unless the button is pressed
    var blockParagraph = document.getElementById("bpReservoirEngineer");
    var blockParagraphButton = document.getElementById("bpReservoirEngineerButton")
    console.log(blockParagraphButton)
    // get the data of whether the paragraph is visible or hidden
    var blockParagraphDisplaySetting = blockParagraph.style.display;

    if (blockParagraphDisplaySetting == "block") {
        blockParagraph.style.display = "none";
        blockParagraphButton.innerHTML = "▽"
    }
    else {
        blockParagraph.style.display = "block";
        blockParagraphButton.innerHTML = "△"
    }
}

